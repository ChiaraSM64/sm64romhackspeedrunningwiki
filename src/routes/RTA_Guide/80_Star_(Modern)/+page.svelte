<h1>RTA Guide/80 Star Modern</h1>

<p>
    "80 Star Modern" is a new variant of the "intended star count any%" category. Unlike in "80 Star Legacy," BLJs and sequence breaks
    are now allowed except to skip the 80-star door.
</p>

