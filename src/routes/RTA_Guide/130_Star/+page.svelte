<h1>RTA Guide/130 Star</h1>

<p>
    130 Star is the 100% category for Super Mario Star Road. You collect 119 stars, then beat Bowser to get the 120th star and
    unlock the postgame. After that, you get the Hidden Palace Finale star and get the 9 replicas (there's 1 replica in every
    non-course level). The run ends upon grabbing the 130th star.
</p>

