<h1>RTA Guide/0 Star No HyperJump</h1>

<p>
    0 Star No HyperJump is the second fastest category of Super Mario Star Road and is the fastest possible category for Beginners.
    In this category you do 4 different skips (1 door clip and 3 BLJs) to enter all 3 Bowser stages and finish the game.
</p>

