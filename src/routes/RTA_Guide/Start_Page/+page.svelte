<h1>RTA Guide</h1>

<p>
    Here you will find everything you need to know about Super Mario Star Road Speedrunning. Categories, Routes, ROM Versions, N64/VC compatibility...
</p>

<h2>Available Platform</h2>

<p>
    <span>For Super Mario Star Road you can run either on Emulator, either on Virtual Console. There is a N64 compatible version but this one isn't allowed for speedruns.</span>
</p>

<h2>Versions</h2>

<p>
    <span>Only v1.0, v1.01, Deluxe and Final versions are allowed.</span>
</p>