<h1>RTA Guide/0 Star HyperJump</h1>

<p>
    0 Star HyperJump is the fastest category of Super Mario Star Road and is a way harder Any% alternative.
    In this category you do 1 major glitch called "HyperJump" which makes you able to skip the first 2 Bowser stages. This makes it possible to finish the game with 0 keys and in under 3 minutes.
</p>

